#!/bin/bash                                                                                              

awk -v RS='\r\n' -v name="$1"   '{
    line[NR] = $0
    if($4==name)
        {
        tmp = line[NR-1]
        getline
        for(i=1;i<=tmp;i++)
            {
    		if($1=="C")
    			{
    			printf(" %2s 6.0 %14.12f %14.12f %14.12f\n", $1"\t",$2"\t",$3"\t",$4);
    			}
